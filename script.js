const articleThreeHeight = $articleThree.clientHeight;

document.addEventListener('scroll', transitionTrigger);

// function to check position of an element
function checkPosition() {
    const viewportHeight = window.innerHeight;
    const scrollY = window.scrollY;
    const scrollPosition = scrollY + viewportHeight;
    const articleThreePosition = $articleThree.getBoundingClientRect().top;

    console.log(scrollPosition, articleThreePosition)

    if (scrollPosition > articleThreePosition) {
        return true;
    }
    return false;
}


// function to toggle between fade-in fade-out classes to trigger animation on element 3
function transitionTrigger() {
    if (checkPosition()) {
        $articleThree.classList.remove('hide');
        $articleThree.classList.add('reveal');
    }
    else {
        $articleThree.classList.remove('reveal');
        $articleThree.classList.add('hide');
    }
}